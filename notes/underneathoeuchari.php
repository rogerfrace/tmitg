<h1>Places in Between: “Underneath” and “O Euchari” (tracks 4 and 11)</h1>

<p>Here are some of the influences, decisions and processes that went into creating some of the music and lyrics for <i>Places in Between</i>.</p>

<hr>

<h2>The Music (by Roger)</h2>

<p>One of our strategies when making a new album is to go back and listen to old unfinished songs and musical doodles to see if any new inspiration strikes to complete what was once incomplete or wasn’t working. That was the case here with “O Euchari” from 2004 and “Underneath” from 2001. With how both of these songs turned for the album I’m really glad we waited because they needed a lot of extra fleshing out and arrangements to make them more interesting and just, well, good.</p>

<p>“Underneath” was originally just guitar-bass-vocal, so the drums and piano layers in the final version are new, as are Summer’s vocal harmonies.</p>

