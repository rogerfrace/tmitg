<h2>The Lyrics (by Summer)</h2>

<p>When I was first playing around with the melodies on this piece I sang a whole bunch of different stuff. Just sort of improvised and played around and ultimately I had a whole bunch of vocal melodies. I was talking to Roger about it and he said, “you know, it’s basically a round.” That sort of changed everything for me. It’s a round. It’s a round! So I took my favorite five pieces and strung them together and offset them five times and played it back. It sounded super cool so I knew I had something. Then it came to writing the lyrics. I listened a bit to some of the improv things I sang and there were some ideas that were there, but I wanted to play with that idea of a round and of the circular nature of so many things in the world. Then I was brought back to the idea of Carl Sagan that we are star stuff, that ultimately everything that we are has come from the wider universe and will go back into the universe again. I was so inspired that I actually explore similar themes on “Ad Adstra Per Aspera.”</p>

