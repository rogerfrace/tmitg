<p>“O Euchari” was more complete in its original version: the drone, vintage drum loop, strings, guitar, woodwinds, and piano all being present. The final version added all of the rock and orchestral drums, plus some refinement to the general arrangement. This was also the last song that we completed for the album.</p>

<hr>

<h2>The Lyrics (by Summer)</h2>

<p>I had completely forgotten about both of these tracks when Roger pulled them up from the depths of the studio computer. “Underneath” already had complete lyrics and vocals but the performance wasn’t so great and it was only one vocal track—no harmonies like you hear in the final version. Honestly, I couldn’t say anything about the inspiration for the “Underneath” lyrics as I really can’t recall what was going through my head 19 years ago. But when I heard them again, they felt very timely and in line with other themes on the album. I think there was some initial concern (by Roger) that the vocals would need a complete re-write. This has happened before, in fact. “<a rel="noopener external" href="https://tmitg.bandcamp.com/track/in-the-vanir" target="_blank">In the Vanir</a>” from <i>Before and After the Storm</i> was dredged up for that album and the vocal melody and lyrics were completely scrapped, save for the very last section (“I have brought myself to this point&hellip;”). But no rewrites were required. I just re-recorded the vocals and then added all the harmony parts.</p>

<p>“O Euchari” was something I had brought to Roger many years ago. I’ve long been a fan of Hildegard von Bingen and wanted to do a track. Obviously this is something that Mirabilis has tackled and Roger and I have reinterpreted classical vocal pieces in the past (“<a rel="noopener external" href="https://tmitg.bandcamp.com/track/miserere-mei" target="_blank">Miserere Mei</a>”) with great success. Learning and then singing “O Euchari” was harder than I anticipated and I spent a lot of hours learning and practicing as well as trying to fit a chant into modern music with meter. Hildegard’s chants have no meter, no tempo, and the notes have no durations. It’s up to the singer how to perform it. Ultimately, I think it fit really well, but it was a challenge figuring out how to do it without it sounding strange and off kilter.</p>

