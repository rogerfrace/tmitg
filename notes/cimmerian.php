<h1>Before and After the Storm: "Cimmerian" (track 1)</h1>

<p>Here are some of the influences, decisions and processes that went into creating some of the music and lyrics for <i>Before and After the Storm</i>.</p>

<hr>

<h2>The Music (by Roger)</h2>

<p>"Cimmerian" was always going to be a long/epic song. It was also the song that got the ball rolling for creating an album of new material. Of course, the time from my mental inception of this song to actually going into the studio and starting it was about 6 months. Ultimately did the song turn out like it sounded in my head? No. Does it ever? No.</p>

<p>I was very much channeling Swans with this song, especially songs like "<a rel="noopener external" href="https://apple.co/3tTkWeF" target="_blank">We Will Survive</a>" from <i>White Light From The Mouth Of Infinity</i>. There's the repetition/drone of the guitars and the tribal yet precise drumming that has always stood out to me. This album represents my favorite period for this band and I was lucky enough to see them live in NYC at CBGB during this time. If my copy of White Light had been anything other than a CD I would have worn it out during my time in grad school.</p>

<p>The orchestral elements that appear later in the song are very much typical tMitG style. After all, we talk in our bio about the exploration of technology and nature which I often express by mixing natural and unnatural sounds — of course in most cases the "natural" sounds are also produced from a synth, but they're still emulating natural sounds.</p>

<p>The song ends in a grandiose fashion with all the instruments playing — again, pretty typical tMitG. If you haven't noticed before, I like things big, though my favorite word and the effect I like to achieve is "glorious." I thought of this particular adjective when trying to describe the sound I desired back on <i>One Winter's Night&hellip;</i> when trying to describe the chorus on "<a rel="noopener external" href="https://apple.co/3sQuKEX" target="_blank">The Sleep of Angels</a>" — big, beautiful and emotional all rolled together.</p>

<p>Even though I've listened to "Cimmerian" many hundreds of times by now, I still find myself welling-up a little inside when everything kicks in and Summer sings the last part of the song (starting around 5:03). But *that* is the point, isn't it?</p>

<hr>

<h2>The Vocals (by Summer)</h2>

<p>When we first starting talking about an album and I heard this song, I had the idea of maybe doing a concept album with the songs being inspired by different mythologies and stories from ancient cultures. It's something that springs up for me a lot, lyrically and particularly ancient Greece, but I wanted something different.</p>

<p>I stumbled across a mention of the Cimmerians, which, according to the Greek scholar Herodotus, were a group of people that inhabited the area known today as Ukraine and Russia. There's scholarly argument today as to whether or not these people actually existed. The Cimmerians are also mentioned in Homer's <i>Odyssey</i> as a group of people living in fog and darkness near the gates of Hades (the underworld).</p>

<p>I sort of combined the ideas of both the historical and mythological in the lyric writing for Cimmerian and thought about dark vs. light. I really like the way the song has different elements of darkness and lightness and I wanted to capture that with the lyrics.</p>

<p>In terms of the vocal melody and some of the vocal styling I employ, I really wanted to have a large contrast between the dark and light sections with the dark sections featuring a harsh whisper with a strong chest voice and the lighter elements using softer head voice and a higher register. For the end where all the elements came together, I use a combination of the two styles.</p>

