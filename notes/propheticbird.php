<h1>Places in Between: “Prophetic Bird” (track 5)</h1>

<p>Here are some of the influences, decisions and processes that went into creating some of the music and lyrics for <i>Places in Between</i>.</p>

<hr>

<h2>The Music (by Summer)</h2>

<p>I try to “practice” my songwriting on a regular basis. In fact, I once went through a period of time where I vowed to write something every day. That lasted for maybe a week! But I always know that we want to have new tMitG and new Mirabilis in progress so I try to write when the mood strikes me, which sometimes isn’t that often. In any case, this was actually a “doodle” that I sent to Dru for Mirabilis to use, but during the early writing process for Places in Between I had given a couple promising tracks to Roger to work with. He sort of changed some of the sounds, gave it structure and added some additional drums to help fill things out before giving it back to me to finish with words and vocals. This was actually one of the earlier tracks completed for the album. I recall when I traveled to Portland to perform with Mercury’s Antennae in 2018 we had four complete songs that I previewed for Dru, Erick, and Clovis while I was there and this was one of them.</p>

<hr>

<h2>The Lyrics (by Summer)</h2>

<p>I had played around a lot of various mythologies of the world in my lyric writing on Before and After the Storm almost to the point of it becoming a bit of a concept album in that regard. I really enjoyed that a lot and so I turned to other cultures for further inspiration. When I was investigating Slavic folklore, I found the myth of Gamayun who is, well, a prophetic bird! At the end of the writing process I had a LOT of words somehow and when I started fitting them to music, it appeared that the pacing of the vocals were going to be really fast with some more complex rhythms. I think it’s really different for me and I’m really happy with how it turned out. In fact, the first time Dru heard it she commented that it was a lot of words!</p>

