<h1 class="wai"><abbr title="the Machine in the Garden">tMitG</abbr> Biography</h1>

<div class="twocols">
<p>The Machine in the Garden is a gothic/darkwave duo featuring Roger Frac&eacute; and Summer Bowman. Since their formation, Roger and Summer have developed and advanced their unique style through years of collaborating and intertwining their musical tastes.</p>

<p>Initially a solo endeavor by Roger Frac&eacute;, the band&rsquo;s debut, 1994&rsquo;s <em>Veils and Shadows EP</em>, blended the dark elements of goth with new wave and industrial tinges. Summer Bowman joined Frac&eacute; in 1997 for their first full length album, <em>Underworld</em>. The dark electronics of the debut EP, which garnered acclaim from press and radio, were still there, but multi-layered with an atmospheric mood and style, giving the CD an original sound transcending pre-existing genres.</p>

<p>The band signed to the New York-based label Middle Pillar Presents in 1998. <em>One Winter&rsquo;s Night&hellip;</em>, released in 1999, honed the band&rsquo;s sound in a more ethereal direction, merging intricate musical structures with emotional lyrics. The band followed up quickly with an enhanced CD in 2000 entitled <em>Out of the Mists</em>. In its creative exploration, the band made a video for the haunting and melodic song, &ldquo;The Unaware.&rdquo;</p>

<p>Two more releases on Middle Pillar followed. 2002&rsquo;s <em>Asphodel</em> found the band exploring songs of love and desire, sorrow and despair, always searching for the quiet beauty underneath the darkness. The critically acclaimed <em>Shadow Puppets</em>, released in 2005, is a seamless amalgam of electronics, ethereal melodies and sensuous rhythms, focusing more on electronics and loops than previous albums. It is dynamic, anthemic, and yet at times introspective, propelled by Bowman&rsquo;s dazzling vocals. It was the band&rsquo;s final release with Middle Pillar.</p>

<p>2007 marked the 15th anniversary of Frac&eacute;&rsquo;s formation of the band as well as the 10th anniversary of Bowman&rsquo;s joining. For this special occasion, the band released <em>XV</em>, a special limited-edition CD with 15 tracks spanning the band&rsquo;s 15-year history, including demos, remixes, previously unreleased outtakes and two brand-new songs.</p>

<p>After three years, the Machine in the Garden returned in December 2010 with their first-ever single and first-ever digital-only release, <em>In the Vanir</em>. This is followed in June 2011 by <em>Before and After the Storm</em>, their first full album of new material since 2005. Musically it can be described as &ldquo;old-school, evolved,&rdquo; but ultimately continues the band&rsquo;s nearly 20-year style of uniquely mixing multiple genres.</p>

<p>The name &ldquo;the Machine in the Garden&rdquo; refers to the progress of technology and its relationship with nature. Each release explores this relationship between the mechanical, natural and spiritual worlds and further elucidates the philosophy behind the band. Ever evolving and pushing the limits of the ethereal/darkwave genre, the Machine in the Garden strives to explore all aspects of electronic music while continuing to compose songs that endure.</p>
