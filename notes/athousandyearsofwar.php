<h1 itemprop="name">Places in Between: “A Thousand Years of War” (track 2)</h1>

<div itemprop="text">
<p>Here are some of the influences, decisions and processes that went into creating some of the music and lyrics for <i>Places in Between</i>.</p>

<hr>

<h2>The Music (by Summer)</h2>

<p>Again, this was one of the doodles that I had hanging around that I ended up sending Roger’s way. This was actually later in the writing process for the album. Really, we had four or five songs done I think for several years and then just in the last year we really sped forward and got to work. So this one was completed in just the last couple of months of writing and recording. We had gone back and forth about EP versus full-length and I was pretty adamant about doing a full-length album. I think Roger wasn’t writing a lot of music so I started going through all my doodles to see if I had anything that sounded like it belonged more to tMitG than to Mirabilis. I gave Roger two or three things I think and this was one of them and the only one that came back to me. Like on “Prophetic Bird,” he arranged the song and gave it structure, changed some of the sounds and fleshed it out a bit.</p>

<hr>

<h2>The Lyrics (by Summer)</h2>

<p>Again, I was inspired by mythology. I was actually familiar with this mythology from Hindu scriptures from a song by Vas called “<a rel="noopener external" href="https://apple.co/3dTM0oG" target="_blank">Amrita (Churning the Sea of Milk)</a>.” I had been curious about what that meant so I looked it up and was really interested in it. But this was years ago and I had forgotten about it (that album came out in 2004). Our daughter had recently gotten a book about mythology geared towards kids and this myth was in there so I was reintroduced to it. I was immediately inspired and started writing words and when the music to this song came back to me, I knew it was the right fit.</p>
