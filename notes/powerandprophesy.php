<div itemprop="text">
<p>Here are some of the influences, decisions and processes that went into creating some of the music and lyrics for <i>Before and After the Storm</i>.</p>

<hr>

<h2>The Music (by Roger)</h2>

<p>I've written before about the old-school influences on this album, and I think "Power and Prophesy" is a big part of that. I wanted to do something straight out of the old-school goth rock playbook, ala The Sisters of Mercy/The Mission/The Wake, etc. It could even be argued that tMitG hasn't done a "rock" song with this level of energy for a long time, perhaps even back to "<a rel="noopener external" href="https://tmitg.bandcamp.com/track/a-touch-of-heaven" target="_blank">A Touch of Heaven</a>" on the <i>Veils and Shadows EP</i>.</p>

<p>The main guitar riff was very much in the vein of The Sisters of Mercy "<a rel="noopener external" href="https://apple.co/32PYKGw" target="_blank">First and Last and Always</a>" or "<a rel="noopener external" href="https://apple.co/3vluaAM" target="_blank">Nine While Nine</a>," just with more distortion, and in that regard it does harken back to my picking style in the old days like "A Touch of Heaven." The rest of the song was wrapped around the guitar riff and simply tried to keep up the energy. The song morphed over time with the break/bridge/solo being the final part added after a dozen mixdowns had already been done.</p>

<hr>

<h2>The Vocals (by Summer)</h2>

<p>I've talked about the mythology inspirations for many of the lyrics on the album. This is not one of those songs. People often ask how I write words. Do I hear the music and write words from it or do I write words and later fit them to music. I actually do both of those things. The vast majority of the album was written in the former way. This song is an exception. I had the words simmering around in a file on my computer. They were just waiting for the right song. They were a little cheesier — a little gothier at first and that mostly worked given how goth rock the song sounds. But I didn't want them to be too cliche so I changed them up a bit.</p>

<p>The delivery is really pretty straight forward. I didn't want to be too crazy with them. I felt like there was a lot of open space at the beginning and end so I added the "oh's" and I think it adds a dimension to the song that's a little different for me and my usual vocal style.</p>
