<h1>Before and After the Storm: "Cimmerian (Reprise)" (track 8)</h1>

<p>Here are some of the influences, decisions and processes that went into creating some of the music and lyrics for <i>Before and After the Storm</i>.</p>

<hr>

<h2>The Music (by Roger)</h2>

<p>"Cimmerian (Reprise)" is probably the most directly influenced song on the album, mainly because it wouldn't exist if I hadn't heard the inspiration. The Streetlight Farm had posted a copy of "<a rel="noopener external" href="https://apple.co/2Qpcfut" target="_blank">Gight Vaulting</a>" by Emmalee Crane from her album <i>Formantine</i> on Soundcloud (I think). While I'm not normally big on drone/ambient music, not for lack of exposure (thanks Eno!), I found this particular composition both interesting and beautiful.</p>

<p>Drone/ambient is also a genre I don't feel I do particularly well as I prefer to have more structure and rhythm to my compositions than that genre generally affords — you have to go back to 1996 with "<a rel="noopener external" href="https://apple.co/3dPftQK" target="_blank">Spiritus ex Obitus Sanctus</a>" to hear the extent of my previous work (highly recommend listening with headphones, fwiw — lots of panning in there).</p>

<p>I can't remember why I picked "Cimmerian" to adapt — maybe I could hear the song in my head without the drums and liked it. The reprise version was done in the same sequence file as the original mix and I just left a 4 measure buffer before the start of the ambient version. All of the instruments are on the same tracks as their originals so the mix is actually the same, and most of the notes/parts are identical as well, dragged over from earlier in the song. The opening notes and some other other layers are new, but use the same sounds but lower, single notes as a base.</p>

<p>The only thing that was done on new tracks were the vocals which Summer wanted to add (I wasn't originally planning for any). They are a new performance with different effects than the original version of the song, but they needed to be softer and more ethereal to match the rest of the mix. Going back to <i>Underworld</i>, again, she did something similar on "<a rel="noopener external" href="https://apple.co/3vo7Fer" target="_blank">Altered Form</a>."</p>

My original plan for the song order was to have "Cimmerian (Reprise)" as track 2, a similar strategy/flow as Christian Death's <a rel="noopener external" href="https://apple.co/332hzGR" target="_blank"><i>Ashes</i></a>, but ultimately we decided to make it the last track providing a nice wrapper for the complete album.

