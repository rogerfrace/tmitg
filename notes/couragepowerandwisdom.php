<h1>Places in Between: “Courage, Power, and Wisdom” (track 8)</h1>

<p>Here are some of the influences, decisions and processes that went into creating some of the music and lyrics for <i>Places in Between</i>.</p>

<hr>

<h2>The Music (by Roger)</h2>

<p>This was the first song written for the new album, way back in 2013, though it ended up the second-to-last to be finished. Structurally it doesn’t follow a typical verse-chorus structure but instead follows a simple chord progression originally based on the 1-4-1-5-1 pattern, but ended up a bit different:</p>

<figure>
<img src="cpw.png" alt="">
<figcaption>Courage, Power, and Wisdom’s chords, abbreviated<figcaption>
</figure>

<p>The original intent of the song was much more minimal with the use of these basic chord and long reverb/delay with each chord lasting 16 measures. It ended up growing to include the guitar and drum parts and was eventually worked into what is on the album.</p>

<hr>

<h2>The Lyrics (by Summer)</h2>

<p>I am a longtime fan of the video game series, “The Legend of Zelda,” and when the most recent installment came out (“Breath of the Wild”) I was really inspired to see if I could find a way to write some lyrics around those ideas. At the end of the day, I had a TON of lyrics—more than I ended up using for the song. This song was really challenging for me from a melody point of view. I had the rhythms worked out—how the words would fit, but I couldn’t figure out how to make a cohesive melody out of the song the way it was originally structured. It was really confusing to me and I went back and forth with Roger about how to make the song a little more user-friendly from a vocal melody perspective. I think at the end of the day, the song turned out much stronger for some of the back and forth work we did, but it felt really fraught at the time, not because we were arguing or anything (we weren’t) but because it just felt like the song was giving me so much grief when it shouldn’t have been that tough!</p>

