<h1 class="wai">the Machine in the Garden Biography</h1>

<div class="twocols">
<p>The Machine in the Garden is a gothic/darkwave duo featuring Roger Frac&eacute; and Summer Bowman. Since their formation, Roger and Summer have developed and advanced their unique style through years of collaborating and intertwining their musical tastes.</p>

<p>Initially a solo endeavor by Roger Frac&eacute;, the band&apos;s debut, 1994&apos;s <i>Veils and Shadows EP</i>, blended the dark elements of goth with new wave and industrial tinges. Summer Bowman joined Frac&eacute; in 1997 for their first full-length album, <i>Underworld</i>. The dark electronics of the debut EP were still there, but multi-layered with an atmospheric mood and style, giving the release an original sound transcending pre-existing genres.</p>

<p>The band signed to the New York-based label Middle Pillar Presents in 1998 and released four more albums: <i>One Winter&apos;s Night&hellip;</i> in 1999, <i>Out of the Mists</i> in 2000, <i>Asphodel</i> in 2002 and <i>Shadow Puppets</i> in 2005.</p>

<p>2007 marked the 15th anniversary of Frac&eacute;&apos;s formation of the band. For this special occasion, the band released <i>XV</i> featuring 15 tracks spanning the band&apos;s 15-year history, including demos, remixes, previously unreleased outtakes and two brand-new songs.</p>

<p>They returned in June 2011 with <i>Before and After the Storm</i>, their first full album of new material since 2005. Musically the eight tracks felt &ldquo;old-school, evolved,&rdquo; but ultimately continued the band&apos;s style of uniquely mixing multiple genres.</p>

<p>Nine years later and the band is back with thirteen new songs exploring the variety of styles&mdash;from goth rock to electronic ethereal&mdash;the band is known for. Coming in at over an hour in length, <i>Places in Between</i> is chock full of epic electronics, sweeping guitars and shining vocals. The album has everything fans have come to appreciate about the band in their nearly 30 year history, while still sounding elevated and modern.</p>

<p>The name &ldquo;the Machine in the Garden&rdquo; refers to the progress of technology and its relationship with nature. Each release explores this relationship between the mechanical, natural, and spiritual worlds. Ever evolving and pushing the limits of the ethereal/darkwave genre, the Machine in the Garden strives to explore all aspects of electronic music while composing songs that endure.</p>

