<h1>Places in Between: “Find a Way” (track 12)</h1>

<p>Here are some of the influences, decisions and processes that went into creating some of the music and lyrics for <i>Places in Between</i>.</p>

<hr>

<h2>The Music (by Roger)</h2>

<p>This song began on the real piano in our living room. I was just playing around with the slow notes and some progressive descending chords. Eventually things started to sound good and I was enjoying improvising on the pattern. I eventually grabbed my phone to record what I was doing, speaking references on top of the music so I would know what notes and chords I was playing. From there the voice memo was imported into my DAW and I set about recreating and arranging the song with a sampler piano sound.</p>

<hr>

<h2>The Lyrics (by Summer)</h2>

<p>There are a couple of sets of “timely” lyrics on this album (see also “Broken Days” and “Resist”). I don’t always write about things that are going on in the world at large. In fact, I guess I should say I never have because now that I think about it, I can’t actually think of any other lyrics from previous releases that dealt with current world events. But with everything *gestures broadly* it just became really hard not to touch on that in my lyric writing. I think the song speaks to that part of me that is depressed and unhappy one day and then feeling fine and hopeful the next. Or even unhappy one moment and okay a few minutes later. It all ebbs and flows and hopefully we CAN find a way to figure everything out. I will say that without the pandemic, you wouldn’t have this album. Make of that what you will, I suppose.</p>

